function [A]=cgBha(X,AJ,W,R,T)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% Bhadeshia: CG DOUBLE PRECISION FUNCTION GIVING LFG  LN(ACTIVITY) OF CARBON IN AUSTENITE
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
%
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Adapted to Octave by a.d. boccardo
% date: 03-16-2015
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
  
  if (X<=1e-10)
    A=log(1e-10);
  else
    DG=sqrt(1-2.*(1+2.*AJ)*X+(1+8.*AJ).*X.*X);
    EG=5.*log((1-2.*X)./X)+6.*W./(R.*T)+(38575-13.48.*T)/(R.*T);
    A=EG+6.*log((DG-1+3.*X)./(DG+1-3.*X));
  end
  
end
  