function [AFE]=afegBha(XEQ,AJ)

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
%  H. K. D. H. Bhadeshia, 1984
%  Natural logarithm of the activity of iron in austenite
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
%
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Adapted to Octave by a.d. boccardo
% date: 03-16-2015
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

  DEQ=sqrt(1-2.*(1+2.*AJ).*XEQ+(1+8.*AJ).*XEQ.*XEQ);
  TEQ=5.*log((1-XEQ)./(1-2.*XEQ));
  TEQ=TEQ+6.*log((1-2.*AJ+(4.*AJ-1).*XEQ-DEQ)./(2.*AJ.*(2.*XEQ-1)));
  AFE=TEQ;

end